package 조합수;

import java.util.*;

// 메모이제이션
public class Main2 {
	// T.DFS(n, r)로 접근하기 때문에 인스턴스 변수로 선언해도 가능
	int[][] dy = new int[35][35];	// 넉넉하게 35로 잡음(n 범위확인)
	
	public int DFS(int n, int r) {
		if(dy[n][r] > 0) return dy[n][r];
		if(n == r || r == 0) return 1;
		else return dy[n][r] = DFS(n - 1, r - 1) + DFS(n - 1, r);
	}
	
	public static void main(String[] args) {
		Main2 T = new Main2();
		Scanner kb = new Scanner(System.in);
		int n = kb.nextInt();
		int r = kb.nextInt();
		System.out.println(T.DFS(n, r));
	}

}
